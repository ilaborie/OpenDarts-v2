package models.dart;

/** The Interface IDart. */
public interface IDart {

	/** Gets the sector.
	 * 
	 * @return the sector */
	DartSector getSector();

	/** Gets the zone.
	 * 
	 * @return the zone */
	DartZone getZone();

	/** Gets the score.
	 * 
	 * @return the score */
	int getScore();

	/**
	 * Gets the color.
	 *
	 * @return the color
	 */
	Color getColor();

}
